def convert_to_sp5(sp_data, asset_data, handle_missing='skip'):
    """
    Converts a USD pricing history of any asset, good, service, etc., into SP5 units.
    
    SP5 is defined as the 2-year moving average of the S&P 500, used as a denominator to normalize
    for productivity and dollar debasement. The conversion is: sp5_price = usd_price / sp5_value,
    where sp5_value is the 2-year moving average at the corresponding date.
    
    This function assumes monthly data alignment. It matches dates exactly by year and month.
    The output is suitable for charting or further financial analysis, maintaining the same
    structure as the input asset_data but with the price converted to SP5 units.
    
    Parameters:
    - sp_data (list of lists): Historical S&P moving average data.
      Format: [[year (int), month (int), 1_year_ma (float), 2_year_ma (float), 4_year_ma (float)], ...]
      Must be sorted chronologically. The 2_year_ma column is used for SP5.
      Example: [[1875, 1, 4.56, 4.662083333333334, 4.775208333333333], ...]
    - asset_data (list of lists): USD pricing history of the asset.
      Format: [[year (int), month (int), usd_price (float)], ...]
      Must be sorted chronologically.
      Example: [[2000, 1, 1500.0], [2000, 2, 1520.5], ...]
    - handle_missing (str, optional): How to handle missing SP5 values for asset dates.
      - 'skip' (default): Skip entries where no matching SP5 date is found.
      - 'raise': Raise a ValueError if any asset date lacks a matching SP5 date.
    
    Returns:
    - list of lists: Converted data in SP5 units.
      Format: [[year (int), month (int), sp5_price (float)], ...]
      Example: [[2000, 1, 0.987], [2000, 2, 1.002], ...]
    
    Usage Notes:
    - To use this in a Grok instance with tool-use: Paste this function definition into a code_execution tool call,
      then execute it by calling the function with your data, e.g.:
      result = convert_to_sp5(sp_data, asset_data)
      print(result)  # or process further
      Since code_execution is stateful, you can define the function once and reuse it.
    - Ensure data is pre-loaded as Python lists. If loading from JSON, use json.loads().
    - For non-monthly data, you may need to preprocess to align dates.
    - No external libraries required; pure Python.
    
    Raises:
    - ValueError: If handle_missing='raise' and a date mismatch occurs, or if input formats are invalid.
    """
    if not isinstance(sp_data, list) or not sp_data or not isinstance(sp_data[0], list):
        raise ValueError("sp_data must be a non-empty list of lists.")
    if not isinstance(asset_data, list) or not asset_data or not isinstance(asset_data[0], list):
        raise ValueError("asset_data must be a non-empty list of lists.")
    
    # Create a dictionary for quick lookup: (year, month) -> 2_year_ma
    sp_dict = {}
    for row in sp_data:
        if len(row) != 5:
            raise ValueError("Each row in sp_data must have exactly 5 elements: [year, month, 1y, 2y, 4y].")
        year, month = int(row[0]), int(row[1])
        two_year_ma = float(row[3])
        sp_dict[(year, month)] = two_year_ma
    
    # Process asset data
    result = []
    for row in asset_data:
        if len(row) != 3:
            raise ValueError("Each row in asset_data must have exactly 3 elements: [year, month, usd_price].")
        year, month, usd_price = int(row[0]), int(row[1]), float(row[2])
        key = (year, month)
        if key in sp_dict:
            sp5_value = sp_dict[key]
            if sp5_value == 0:
                raise ValueError(f"SP5 value is zero for {year}-{month}, cannot divide.")
            sp5_price = usd_price / sp5_value
            result.append([year, month, sp5_price])
        else:
            if handle_missing == 'raise':
                raise ValueError(f"No matching SP5 data for date {year}-{month}.")
            # Else 'skip': do nothing
    
    return result

